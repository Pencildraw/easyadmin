<?php

namespace app\admin\controller\dealer;

use app\common\controller\AdminController;
use EasyAdmin\annotation\ControllerAnnotation;
use EasyAdmin\annotation\NodeAnotation;
use think\App;

/**
 * @ControllerAnnotation(title="mall_order")
 */
class order extends AdminController
{

    use \app\admin\traits\Curd;

    public function __construct(App $app)
    {
        parent::__construct($app);

        $this->model = new \app\admin\model\mall\order();
        
    }

    /**
     * @NodeAnotation(title="列表")
     */
    public function index()
    {
        if ($this->request->isAjax()) {
            if (input('selectFields')) {
                return $this->selectList();
            }
            list($page, $limit, $where) = $this->buildTableParames();
            $count = $this->model
                ->where($where)
                ->where('pay_status',1)
                ->count();
            $list = $this->model
                ->field('ea_mall_order.* 
                    ,(SELECT name FROM ea_company_identity WHERE id = ea_mall_order.supplier_id) AS identity_supplier
                    ,(SELECT phone FROM ea_company_identity WHERE id = ea_mall_order.supplier_id) AS supplier_phone
                    ,(SELECT name FROM ea_company_identity WHERE id = ea_mall_order.shop_id) AS identity_shop
                    ,(SELECT phone FROM ea_company_identity WHERE id = ea_mall_order.shop_id) AS shop_phone
                ')
                // ,(SELECT name FROM ea_company_identity WHERE id = ea_mall_order.shop_id) AS identity_shop 
                ->where($where)
                // ->where('pay_status',1)s
                ->page($page, $limit)
                ->order($this->sort)
                // ->fetchsql(true)
                ->select();
            // print_r($list); exit;
            $data = [
                'code'  => 0,
                'msg'   => '',
                'count' => $count,
                'data'  => $list,
            ];
            return json($data);
        }
        $this->layoutBgColor();
        return $this->fetch();
    }

    /**
     * @NodeAnotation(title="添加")
     */
    public function add()
    {
        if ($this->request->isPost()) {
            $post = $this->request->post();
            $rule = [];
            $this->validate($post, $rule);
            try {
                $save = $this->model->save($post);
            } catch (\Exception $e) {
                $this->error('保存失败:'.$e->getMessage());
            }
            $save ? $this->success('保存成功') : $this->error('保存失败');
        }
        return $this->fetch();
    }

    /**
     * @NodeAnotation(title="编辑")
     */
    public function edit($id)
    {
        $row = $this->model->find($id);
        empty($row) && $this->error('数据不存在');
        if ($this->request->isPost()) {
            $post = $this->request->post();
            $rule = [];
            $this->validate($post, $rule);
            try {
                $save = $row->save($post);
            } catch (\Exception $e) {
                $this->error('保存失败');
            }
            $save ? $this->success('保存成功') : $this->error('保存失败');
        }
        $this->assign('row', $row);
        return $this->fetch();
    }
}